Logger initLogger(void *logFunction) {
	Logger _logger;
	_logger.log = logFunction;
	_logger.setLevel = _setLevel;

	return _logger;
}

/******************************************************************************
**                      LOCAL FUNCTIONS                                  	 **
******************************************************************************/
void _setLevel(Logger *this, LoggerLevel level) {
	if(this->level != level) {
		this->level = level;

		switch(level) {
			case LOGGER_ERROR: 	this->levelMsg = "[ERROR]: "; 	break;
			case LOGGER_WARN:	this->levelMsg = "[WARN]: "; 	break;
			case LOGGER_SYSTEM:	this->levelMsg = "[SYSTEM]: "; 	break;
			case LOGGER_INFO:	this->levelMsg = "[INFO]: "; 	break;
		}
	}
}

